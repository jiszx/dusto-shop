package com.hhnz.order.model;

import java.math.BigDecimal;
import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

public class OmOrderLogistics {
	
    private Long id;

    private String sapDeliveryNo;

    private String site;

    private String logisticsNo;

    private String remark;

    private Date createTs;

    private Long createOid;

    private String logistics;

    @DateTimeFormat(pattern="yyyy-MM-dd HH:mm:ss")
    private Date deliveryTs;
    
    private BigDecimal logisticsCost;
    
    private String driverName;
    
    private String driverTel;
    
    private String plateNo;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getSapDeliveryNo() {
        return sapDeliveryNo;
    }

    public void setSapDeliveryNo(String sapDeliveryNo) {
        this.sapDeliveryNo = sapDeliveryNo;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }

    public String getLogisticsNo() {
        return logisticsNo;
    }

    public void setLogisticsNo(String logisticsNo) {
        this.logisticsNo = logisticsNo;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Date getCreateTs() {
        return createTs;
    }

    public void setCreateTs(Date createTs) {
        this.createTs = createTs;
    }

    public Long getCreateOid() {
        return createOid;
    }

    public void setCreateOid(Long createOid) {
        this.createOid = createOid;
    }

    public String getLogistics() {
        return logistics;
    }

    public void setLogistics(String logistics) {
        this.logistics = logistics;
    }

    public Date getDeliveryTs() {
        return deliveryTs;
    }

	    public void setDeliveryTs(Date deliveryTs) {
        this.deliveryTs = deliveryTs;
    }

	public BigDecimal getLogisticsCost() {
		return logisticsCost;
	}

	public void setLogisticsCost(BigDecimal logisticsCost) {
		this.logisticsCost = logisticsCost;
	}

	public String getDriverName() {
		return driverName;
	}

	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}

	public String getDriverTel() {
		return driverTel;
	}

	public void setDriverTel(String driverTel) {
		this.driverTel = driverTel;
	}

	public String getPlateNo() {
		return plateNo;
	}

	public void setPlateNo(String plateNo) {
		this.plateNo = plateNo;
	}
}