package com.hhnz.account.dto;

import java.math.BigDecimal;
import java.util.Date;

public class AllocationDetailsDTO {
	private Long upaccountId;//上账ID
	private Long merchCustId;//零售商客户ID
	private String merchName;//零售商客户名称
	private BigDecimal allocationAmt;//分配金额
	private Date allocationDate;//分配时间
	private String sapCustomerId;//零售商SAP编码
	private String states;//上账状态
	private String sapVouderId;//上账SAP凭证号
	private String operator;//操作人员
	public Long getUpaccountId() {
		return upaccountId;
	}
	public void setUpaccountId(Long upaccountId) {
		this.upaccountId = upaccountId;
	}
	public Long getMerchCustId() {
		return merchCustId;
	}
	public void setMerchCustId(Long merchCustId) {
		this.merchCustId = merchCustId;
	}
	public String getMerchName() {
		return merchName;
	}
	public void setMerchName(String merchName) {
		this.merchName = merchName;
	}
	public BigDecimal getAllocationAmt() {
		return allocationAmt;
	}
	public void setAllocationAmt(BigDecimal allocationAmt) {
		this.allocationAmt = allocationAmt;
	}
	public Date getAllocationDate() {
		return allocationDate;
	}
	public void setAllocationDate(Date allocationDate) {
		this.allocationDate = allocationDate;
	}
	public String getSapCustomerId() {
		return sapCustomerId;
	}
	public void setSapCustomerId(String sapCustomerId) {
		this.sapCustomerId = sapCustomerId;
	}
	public String getStates() {
		return states;
	}
	public void setStates(String states) {
		this.states = states;
	}
	public String getSapVouderId() {
		return sapVouderId;
	}
	public void setSapVouderId(String sapVouderId) {
		this.sapVouderId = sapVouderId;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	
	
}
