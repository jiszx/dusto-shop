package com.hhnz.salepolicy.model;

public class PolicyLinesDetials {
	private String sku;
	private String verification;
	private String lineid;
	private String materialid;
	private String unit;
	private String price;
	private String primary;
	private String discount;
	private String discountIntensity;
	public String getSku() {
		return sku;
	}
	public void setSku(String sku) {
		this.sku = sku;
	}
	public String getVerification() {
		return verification;
	}
	public void setVerification(String verification) {
		this.verification = verification;
	}
	public String getLineid() {
		return lineid;
	}
	public void setLineid(String lineid) {
		this.lineid = lineid;
	}
	public String getMaterialid() {
		return materialid;
	}
	public void setMaterialid(String materialid) {
		this.materialid = materialid;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getPrimary() {
		return primary;
	}
	public void setPrimary(String primary) {
		this.primary = primary;
	}
	public String getDiscount() {
		return discount;
	}
	public void setDiscount(String discount) {
		this.discount = discount;
	}
	public String getDiscountIntensity() {
		return discountIntensity;
	}
	public void setDiscountIntensity(String discountIntensity) {
		this.discountIntensity = discountIntensity;
	}
	
	
}
