package com.hhnz.virtualwarehouse.model;

import java.math.BigDecimal;
import java.util.Date;

import org.hibernate.validator.constraints.NotBlank;

public class CrmVirtualWarehouse {
    private Long id;

    @NotBlank
    private String factoryCode;

    @NotBlank
    private String custType;

    private String name;

    @NotBlank
    private String materialId;

    private BigDecimal amt;

    private BigDecimal frozenAmt;

    private Date createTs;

    private Long createOid;

    private Date updateTs;

    private Long updateOid;

    private String materialName;
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getFactoryCode() {
        return factoryCode;
    }

    public void setFactoryCode(String factoryCode) {
        this.factoryCode = factoryCode;
    }

    public String getCustType() {
        return custType;
    }

    public void setCustType(String custType) {
        this.custType = custType;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMaterialId() {
        return materialId;
    }

    public void setMaterialId(String materialId) {
        this.materialId = materialId;
    }

    public BigDecimal getAmt() {
        return amt;
    }

    public void setAmt(BigDecimal amt) {
        this.amt = amt;
    }

    public BigDecimal getFrozenAmt() {
        return frozenAmt;
    }

    public void setFrozenAmt(BigDecimal frozenAmt) {
        this.frozenAmt = frozenAmt;
    }

    public Date getCreateTs() {
        return createTs;
    }

    public void setCreateTs(Date createTs) {
        this.createTs = createTs;
    }

    public Long getCreateOid() {
        return createOid;
    }

    public void setCreateOid(Long createOid) {
        this.createOid = createOid;
    }

    public Date getUpdateTs() {
        return updateTs;
    }

    public void setUpdateTs(Date updateTs) {
        this.updateTs = updateTs;
    }

    public Long getUpdateOid() {
        return updateOid;
    }

    public void setUpdateOid(Long updateOid) {
        this.updateOid = updateOid;
    }

	public String getMaterialName() {
		return materialName;
	}

	public void setMaterialName(String materialName) {
		this.materialName = materialName;
	}
}