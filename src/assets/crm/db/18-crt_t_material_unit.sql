-- Create table 物料单位表
drop table T_MATERIAL_UNIT;
create table T_MATERIAL_UNIT
(
  id          VARCHAR2(20) not null,
  code        VARCHAR2(20),
  unit        VARCHAR2(20),
  description VARCHAR2(50),
  constraint PK_T_MATERIAL_UNIT primary key (ID)
);
-- Add comments to the table 
comment on table T_MATERIAL_UNIT
  is '物料产品单位';
-- Add comments to the columns 
comment on column T_MATERIAL_UNIT.description
  is '描述';

commit;

